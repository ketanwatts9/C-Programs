#include <stdio.h>
int main(){
    const int int_const = 25;
    const char char_const = 'A';
    const float float_const = 15.13212;
    printf("printing value of int constant: %d\n",int_const);
    printf("printing value of char constant: %c\n",char_const);
    printf("printing value of float constant: %f\n",float_const);
}